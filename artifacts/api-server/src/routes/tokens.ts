router.post("/admin/tokens/bulk-distribute", async (req, res): Promise<void> => {
  const role = (req.session as any)?.role;
  if (role !== "admin") {
    res.status(403).json({ message: "Admin access required" });
    return;
  }

  const { tokenIds } = req.body;

  if (!tokenIds || !Array.isArray(tokenIds) || tokenIds.length === 0) {
    res.status(400).json({ message: "Token IDs array is required" });
    return;
  }

  try {
    // Get the tokens with user details before updating
    const tokensToDistribute = await Token.find({ 
      _id: { $in: tokenIds }, 
      status: "verified" 
    }).populate('userId');

    if (tokensToDistribute.length === 0) {
      res.status(400).json({ message: "No verified tokens found to distribute" });
      return;
    }

    // Update token status
    const tokens = await Token.updateMany(
      { _id: { $in: tokenIds }, status: "verified" },
      { status: "distributed", updatedAt: new Date() }
    );

    // Send distribution emails to all users
    const emailPromises = tokensToDistribute.map(async (token) => {
      try {
        const user = token.userId as any;
        
        req.log.info({ 
          tokenNumber: token.tokenNumber, 
          userEmail: user?.email, 
          hasRationDetails: !!token.rationDetails,
          rationCardNumber: token.rationCardNumber 
        }, "Preparing to send distribution email");
        
        if (user?.email && token.rationDetails) {
          await sendRationDistributionEmail(user.email, {
            rationCardNumber: token.rationCardNumber,
            cardType: token.rationDetails.cardType,
            familyMembers: token.rationDetails.familyMembers,
            shopName: token.rationDetails.shopName || "Main Ration Center"
          });
          req.log.info({ userEmail: user.email, tokenNumber: token.tokenNumber }, "Distribution email sent successfully");
        } else {
          req.log.warn({ 
            tokenNumber: token.tokenNumber, 
            userEmail: user?.email, 
            hasRationDetails: !!token.rationDetails 
          }, "Skipping email - missing user email or ration details");
        }
      } catch (emailError) {
        req.log.error({ emailError, tokenNumber: token.tokenNumber }, "Failed to send distribution email");
      }
    });

    // Wait for all emails to be sent
    await Promise.allSettled(emailPromises);

    res.json({
      message: `Successfully distributed ${tokens.modifiedCount} tokens and sent notifications`,
      distributedCount: tokens.modifiedCount
    });
  } catch (error) {
    console.error("Bulk distribute error:", error);
    res.status(500).json({ message: "Failed to distribute tokens" });
  }
});

router.get("/admin/dashboard-stats", async (req, res): Promise<void> => {
  const role = (req.session as any)?.role;
  if (role !== "admin") {
    res.status(403).json({ message: "Admin access required" });
    return;
  }

  const allTokens = await Token.find({});

  const today = new Date();
  today.setHours(0, 0, 0, 0);

  const stats = {
    totalTokens: allTokens.length,
    pendingTokens: allTokens.filter((t) => t.status === "pending").length,
    verifiedTokens: allTokens.filter((t) => t.status === "verified").length,
    distributedTokens: allTokens.filter((t) => t.status === "distributed").length,
    todayTokens: allTokens.filter((t) => t.createdAt >= today).length,
  };

  res.json(stats);
});

router.get("/admin/recent-activity", async (req, res): Promise<void> => {
  const role = (req.session as any)?.role;
  if (role !== "admin") {
    res.status(403).json({ message: "Admin access required" });
    return;
  }

  const tokens = await Token.find({})
    .populate('userId', 'name')
    .sort({ updatedAt: -1 })
    .limit(20);

  const activityMap: Record<string, string> = {
    pending: "Token Generated",
    verified: "Token Verified",
    distributed: "Ration Distributed",
  };

  res.json(
    tokens.map((t) => {
      let userName = "Unknown";
      
      if (t.status === "pending") {
        const user = (t.userId as any);
        if (user && user.firstName && user.lastName) {
          userName = `${user.firstName} ${user.lastName}`;
        } else {
          userName = t.holderName || "Unknown";
        }
      } else if (t.status === "verified" || t.status === "distributed") {
        userName = "GovtAdmin";
      }
      
      return {
        id: t._id.toString(),
        tokenNumber: t.tokenNumber,
        action: activityMap[t.status] || t.status,
        userName,
        timestamp: t.updatedAt.toISOString(),
      };
    }),
  );
});

