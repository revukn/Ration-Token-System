const router: IRouter = Router();

function generateTokenNumber(): string {
  const prefix = "TKN";
  const date = new Date();
  const dateStr = `${date.getFullYear()}${String(date.getMonth() + 1).padStart(2, "0")}${String(date.getDate()).padStart(2, "0")}`;
  const random = Math.floor(1000 + Math.random() * 9000);
  return `${prefix}-${dateStr}-${random}`;
}

router.post("/tokens/generate", async (req, res): Promise<void> => {
  const userId = (req.session as any)?.userId;
  if (!userId) {
    res.status(401).json({ message: "Not authenticated" });
    return;
  }

  const parsed = GenerateTokenBody.safeParse(req.body);
  if (!parsed.success) {
    res.status(400).json({ message: parsed.error.message });
    return;
  }

  try {
    // Check if a token already exists for this ration card that is not distributed
    const existingToken = await Token.findOne({
      rationCardNumber: parsed.data.rationCardNumber,
      status: { $in: ["pending", "verified"] }
    });

    if (existingToken) {
      // Check if the existing token is older than 25 days
      const twentyFiveDaysAgo = new Date();
      twentyFiveDaysAgo.setDate(twentyFiveDaysAgo.getDate() - 25);
      
      if (existingToken.createdAt > twentyFiveDaysAgo) {
        res.status(400).json({ 
          message: "Token is already generated by family member check my tokens page",
          existingToken: {
            tokenNumber: existingToken.tokenNumber,
            status: existingToken.status,
            createdAt: existingToken.createdAt
          }
        });
        return;
      }
      // If token is older than 25 days, allow generating a new one
      console.log("Allowing new token generation - existing token is older than 25 days");
    }

    const tokenNumber = generateTokenNumber();

    // Get user and ration card details for ration calculation
    const user = await User.findById(userId);
    const rationCard = await RationCard.findOne({ rationCardNumber: parsed.data.rationCardNumber });
    const cardType = rationCard?.cardType ;
    const familyMembers = rationCard?.familyMembers?.length || 0;
    
    // Calculate ration entitlement
    const entitlement = getRationEntitlement(cardType, familyMembers);
    
    const token = await Token.create({
      tokenNumber,
      rationCardNumber: parsed.data.rationCardNumber,
      holderName: parsed.data.selectedMembers[0] || "Unknown",
      selectedMembers: parsed.data.selectedMembers,
      verificationType: parsed.data.verificationType,
      status: "pending",
      userId,
      rationDetails: {
        cardType,
        familyMembers,
        entitlement,
        month: new Date().toLocaleString('default', { month: 'long' }),
        year: new Date().getFullYear(),
        shopName: user?.fairPriceShop || "FPS-001"
      }
    });

  if (user) {
    sendTokenConfirmationEmail(
      user.email,
      `${user.firstName} ${user.lastName}`,
      token.tokenNumber,
      token.rationCardNumber,
      token.selectedMembers as string[],
      token.verificationType,
    ).catch(() => {});
  }

  res.status(201).json({
    id: token._id.toString(),
    tokenNumber: token.tokenNumber,
    rationCardNumber: token.rationCardNumber,
    holderName: token.holderName,
    selectedMembers: token.selectedMembers,
    verificationType: token.verificationType,
    status: token.status,
    createdAt: token.createdAt.toISOString(),
    updatedAt: token.updatedAt.toISOString(),
    rationDetails: token.rationDetails,
  });
  } catch (error) {
    res.status(500).json({ message: "Failed to generate token" });
  }
});

